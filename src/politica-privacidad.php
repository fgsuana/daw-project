        <h1>Política de privacidad</h1>

	<p>En la página web de empleo para programadores, nos tomamos muy en serio la privacidad de nuestros usuarios. A continuación, te explicamos cómo manejamos la información personal que recopilamos y cómo la protegemos.</p>
	
	<h2>Recopilación de información</h2>
	<p>Cuando te registras en nuestra página web, recopilamos cierta información personal, como tu nombre, dirección de correo electrónico, número de teléfono y ubicación geográfica. También podemos recopilar información sobre tu experiencia laboral, educación y habilidades técnicas. Además, recopilamos información sobre cómo utilizas nuestra página web, incluyendo las páginas que visitas, las búsquedas que realizas y las empresas a las que aplicas.</p>
	
	<h2>Uso de la información</h2>
	<p>Utilizamos la información que recopilamos para ayudarte a encontrar empleo. Compartimos la información que nos proporcionas con empresas que buscan programadores y que podrían estar interesadas en contratarte. También utilizamos la información para mejorar nuestra página web y personalizar tu experiencia como usuario.</p>
	
	<h2>Compartir información</h2>
	<p>No vendemos ni alquilamos tu información personal a terceros. Compartimos tu información únicamente con empresas que buscan programadores y que podrían estar interesadas en contratarte. También podemos compartir información con proveedores de servicios externos que nos ayudan a ofrecer nuestro sitio web y servicios.</p>
	
	<h2>Seguridad</h2>
	<p>Tomamos medidas de seguridad razonables para proteger tu información personal contra la pérdida, el uso indebido y el acceso no autorizado. Sin embargo, ninguna transmisión de datos por Internet es completamente segura, por lo que no podemos garantizar la seguridad de tu información personal transmitida a través de nuestra página web.</p>
	
	<h2>Acceso y control de la información</h2>
	<p>Puedes acceder a la información personal que hemos recopilado sobre ti y actualizarla o eliminarla en cualquier momento. También puedes optar por no recibir correos electrónicos promocionales de nuestra parte.</p>
	
	<h2>Cambios en la política de privacidad</h2>
	<p>Podemos actualizar esta política de privacidad de vez en cuando. Si realizamos cambios significativos en la forma en que tratamos tu información personal, te lo notificaremos mediante una publicación destacada en nuestra página web o por correo electrónico.</p>
	
	<p>Si tienes alguna pregunta o inquietud sobre esta política de privacidad o el manejo de tu información personal, por favor contáctanos a través de la sección de contacto en nuestro sitio web.</p>


